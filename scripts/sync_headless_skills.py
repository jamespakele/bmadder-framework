"""
sync_headless_skills.py — Generate headless (non-interactive) versions of BMAD skills
for use by bmadder.sh and bmadder-iterative.sh pipeline scripts.

The BMAD skills in .agent/skills/ are designed for interactive IDE use — they have
menus, <ask> prompts, HALT conditions, and multi-file step architectures. The bmadder
pipeline scripts need these skills to run headlessly (non-interactive, auto-proceed).

This script:
  1. Reads source skills from .agent/skills/
  2. Inlines step files into consolidated documents
  3. Strips interactive elements (menus, <ask>, user-choice branches, HALTs)
  4. Prepends a headless-mode preamble
  5. Writes output to scripts/headless-skills/
  6. Creates a manifest.json for staleness detection

Usage:
  uv run scripts/sync_headless_skills.py           # Generate all headless skills
  uv run scripts/sync_headless_skills.py --check   # Verify output is up-to-date (CI)
  uv run scripts/sync_headless_skills.py --diff    # Show what would change
  uv run scripts/sync_headless_skills.py --verbose # Show stripping details

Run this after BMAD framework updates, or during bootstrap.
"""

